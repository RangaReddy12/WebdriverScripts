package april3;

public class For_Loop {

	public static void main(String[] args) {
		for(int i=1;i<=5;i++)
		{
			System.out.println("Ranga  "+i);
		}
System.out.println("===========================================");
for(int i=1;i<=5;i+=2)
{
	System.out.println("Ranga  "+i);
}
System.out.println("===========================================");
for(int i=5;i>=1;i--)
{
	System.out.println("Ranga  "+i);
}
	}

}
